# creating fundamentals functions sum, div, product and diff
def som(a, b):
    # return the sum of two numbers
    """
    :param a: number
    :param b: number
    :return: sum of a more b
    """
    print(a + b)


def product(a, b):
    # return the product of a and b
    """
    :param a: number
    :param b: number
    :return: product of a and b
    """
    print(a * b)


def diff(a, b):
    """
    :param a: number
    :param b: number
    :return: return diff from a and b
    """
    # return the diff from a and b
    print(a - b)


def div(a, b):
    """
    :param a: number
    :param b: number
    :return: return divv from a and b
    """
    print(a / b)


