drop database ecommerce;
create database ecommerce;
use ecommerce;


CREATE TABLE `categoria` (`cid` smallint(6) NOT NULL AUTO_INCREMENT,
                                                     `nome` varchar(50) DEFAULT NULL,
                                                                                PRIMARY KEY (`cid`)) ENGINE=InnoDB;


CREATE TABLE `marca` (`mid` smallint(6) NOT NULL AUTO_INCREMENT,
                                                 `nome` varchar(100) DEFAULT NULL,
                                                                             `immagine` varchar(100) DEFAULT NULL,
                                                                                                             `url` varchar(100) DEFAULT NULL,
                                                                                                                                        PRIMARY KEY (`mid`)) ENGINE=InnoDB;


CREATE TABLE `prodotto`
  (`pid` varchar(20) NOT NULL,
                     `nome` varchar(2000),
                            `descr_breve` tinytext,
                            `descr_lunga` text DEFAULT NULL,
                                                       `cid` smallint(6) DEFAULT NULL,
                                                                                 `quantita` smallint(6) DEFAULT NULL,
                                                                                                                `arrivo` smallint(6) DEFAULT NULL,
                                                                                                                                             `impegnati` smallint(6) DEFAULT NULL,
                                                                                                                                                                             `mid` smallint(6) DEFAULT NULL,
                                                                                                                                                                                                       PRIMARY KEY (`pid`),
   FOREIGN KEY (`cid`) REFERENCES `categoria` (`cid`) ON DELETE CASCADE ON UPDATE CASCADE,
   FOREIGN KEY (`mid`) REFERENCES `marca` (`mid`) ON DELETE
   SET NULL ON UPDATE CASCADE) ENGINE=InnoDB;


CREATE TABLE `correlati` (`pid` varchar(255) NOT NULL,
                                             `relpid` varchar(255) NOT NULL,
                                                                   PRIMARY KEY (`pid`,
                                                                                `relpid`),
                          FOREIGN KEY (`pid`) REFERENCES `prodotto` (`pid`),
                          FOREIGN KEY (`relpid`) REFERENCES `prodotto` (`pid`)) ENGINE=InnoDB;


CREATE TABLE `subcat`
  (`parent` smallint(6),
            `child` smallint(6),
                    PRIMARY KEY(`parent`, `child`),
   FOREIGN KEY (`parent`) REFERENCES `categoria` (`cid`) ON DELETE CASCADE ON UPDATE CASCADE,
   FOREIGN KEY (`child`) REFERENCES `categoria` (`cid`) ON DELETE CASCADE ON UPDATE CASCADE) ENGINE=InnoDB;


CREATE TABLE `immagine` (`iid` smallint(6) NOT NULL AUTO_INCREMENT,
                                                    `titolo` varchar(100) DEFAULT NULL,
                                                                                  `indirizzo` varchar(100) DEFAULT NULL,
                                                                                                                   PRIMARY KEY (`iid`)) ENGINE=InnoDB;


CREATE TABLE `prim01`
  (`pid` varchar(20) NOT NULL DEFAULT '',
                                      `iid` smallint(6) NOT NULL DEFAULT '0',
                                                                         `priorita` tinyint(4) DEFAULT NULL,
                                                                                                       `main` tinyint(4) NOT NULL DEFAULT '0',
                                                                                                                                          PRIMARY KEY (`pid`,
                                                                                                                                                       `iid`),
   FOREIGN KEY (`pid`) REFERENCES `prodotto` (`pid`) ON DELETE CASCADE ON UPDATE CASCADE,
   FOREIGN KEY (`iid`) REFERENCES `immagine` (`iid`) ON DELETE CASCADE ON UPDATE CASCADE) ENGINE=InnoDB ;


CREATE TABLE `listino` (`lsid` tinyint(4) NOT NULL AUTO_INCREMENT,
                                                   `nome` varchar(50) DEFAULT NULL,
                                                                              `tassa` smallint(6) DEFAULT NULL COMMENT 'Valore percentuale della tassa da applicare ai prezzi',
                                                                                                                       PRIMARY KEY (`lsid`)) ENGINE=InnoDB;


CREATE TABLE `prezzo`
  (`lsid` tinyint(4) NOT NULL DEFAULT '0',
                                      `pid` varchar(20) NOT NULL DEFAULT '',
                                                                         `valore` float DEFAULT NULL,
                                                                                                PRIMARY KEY (`lsid`,
                                                                                                             `pid`),
   FOREIGN KEY (`pid`) REFERENCES `prodotto` (`pid`) ON DELETE CASCADE ON UPDATE CASCADE,
   FOREIGN KEY (`lsid`) REFERENCES `listino` (`lsid`) ON DELETE CASCADE ON UPDATE CASCADE) ENGINE=InnoDB;


CREATE TABLE `utente`
  (`uid` smallint(6) NOT NULL AUTO_INCREMENT,
                              `nome` varchar(100) DEFAULT NULL,
                                                          `cognome` varchar(100) DEFAULT NULL,
                                                                                         `piva` varchar(20) DEFAULT NULL,
                                                                                                                    `telefono` varchar(20) DEFAULT NULL,
                                                                                                                                                   `rag_soc` varchar(200) DEFAULT NULL,
                                                                                                                                                                                  `cfisc` varchar(20) DEFAULT NULL,
                                                                                                                                                                                                              `email` varchar(255) DEFAULT NULL,
                                                                                                                                                                                                                                           `user` varchar(50) DEFAULT NULL,
                                                                                                                                                                                                                                                                      `pass` varchar(100) DEFAULT NULL,
                                                                                                                                                                                                                                                                                                  `newsletter` tinyint(1) DEFAULT '0',
                                                                                                                                                                                                                                                                                                                                  `lsid` tinyint(4) DEFAULT NULL,
                                                                                                                                                                                                                                                                                                                                                            `enabled` tinyint(1) DEFAULT NULL,
                                                                                                                                                                                                                                                                                                                                                                                         `societa` tinyint(1) DEFAULT '0',
                                                                                                                                                                                                                                                                                                                                                                                                                      `time` DATETIME,
                                                                                                                                                                                                                                                                                                                                                                                                                      PRIMARY KEY (`uid`),
   FOREIGN KEY (`lsid`) REFERENCES `listino` (`lsid`) ON DELETE CASCADE ON UPDATE CASCADE) ENGINE=InnoDB;


CREATE TABLE `indirizzo`
  (`inid` mediumint(9) NOT NULL AUTO_INCREMENT,
                                `uid` smallint(6) DEFAULT NULL,
                                                          `via` varchar(200) DEFAULT NULL,
                                                                                     `numero` varchar(20) DEFAULT NULL,
                                                                                                                  `provincia` varchar(100) DEFAULT NULL,
                                                                                                                                                   `citta` varchar(100) DEFAULT NULL,
                                                                                                                                                                                `cap` varchar(10) DEFAULT NULL,
                                                                                                                                                                                                          `telefono` varchar(20) DEFAULT NULL,
                                                                                                                                                                                                                                         `nome` varchar(100) DEFAULT NULL,
                                                                                                                                                                                                                                                                     `cognome` varchar(100) DEFAULT NULL,
                                                                                                                                                                                                                                                                                                    `principale` tinyint(1) DEFAULT NULL,
                                                                                                                                                                                                                                                                                                                                    PRIMARY KEY (`inid`),
   FOREIGN KEY (`uid`) REFERENCES `utente` (`uid`) ON DELETE CASCADE ON UPDATE CASCADE) ENGINE=InnoDB;


CREATE TABLE `livello`
  (`uid` smallint(6) NOT NULL,
                     `authority` varchar(50) NOT NULL DEFAULT '',
                                                              PRIMARY KEY (`uid`,
                                                                           `authority`),
   FOREIGN KEY (`uid`) REFERENCES `utente` (`uid`) ON DELETE CASCADE ON UPDATE CASCADE) ENGINE=InnoDB;


CREATE TABLE `stato` (`stid` tinyint(4) NOT NULL,
                                        `nome` varchar(50) DEFAULT NULL,
                                                                   PRIMARY KEY (`stid`)) ENGINE=InnoDB;


CREATE TABLE `pagamento` (`paid` smallint(6) NOT NULL,
                                             `nome` varchar(100) NOT NULL,
                                                                 `costo` float NOT NULL DEFAULT '0',
                                                                                                PRIMARY KEY (`paid`)) ENGINE=InnoDB ;


CREATE TABLE `spedizione` (`spid` smallint(6) NOT NULL,
                                              `nome` varchar(100) NOT NULL,
                                                                  `costo` float NOT NULL DEFAULT '0',
                                                                                                 PRIMARY KEY (`spid`)) ENGINE=InnoDB ;


CREATE TABLE `pasp01`
  (`paid` smallint(6) NOT NULL,
                      `spid` smallint(6) NOT NULL,
                                         `paspid` smallint(6) NOT NULL,
                                                              PRIMARY KEY (`paspid`),
   FOREIGN KEY (`paid`) REFERENCES `pagamento` (`paid`) ON DELETE NO ACTION ON UPDATE NO ACTION,
   FOREIGN KEY (`spid`) REFERENCES `spedizione` (`spid`) ON DELETE NO ACTION ON UPDATE NO ACTION) ENGINE=InnoDB;


CREATE TABLE `ordine`
  (`uid` smallint(6) DEFAULT NULL,
                             `oid` mediumint(9) NOT NULL AUTO_INCREMENT,
                                                         `stid` tinyint(4) DEFAULT NULL,
                                                                                   `time` datetime,
                                                                                   `inid` mediumint(9) DEFAULT NULL,
                                                                                                               `paspid` smallint(6) NOT NULL,
                                                                                                                                    PRIMARY KEY (`oid`),
   FOREIGN KEY (`paspid`) REFERENCES `pasp01` (`paspid`) ON DELETE NO ACTION ON UPDATE NO ACTION,
   FOREIGN KEY (`uid`) REFERENCES `utente` (`uid`) ON DELETE CASCADE ON UPDATE CASCADE,
   FOREIGN KEY (`stid`) REFERENCES `stato` (`stid`) ON DELETE CASCADE ON UPDATE CASCADE,
   FOREIGN KEY (`inid`) REFERENCES `indirizzo` (`inid`) ON UPDATE CASCADE) ENGINE=InnoDB;


CREATE TABLE `orpr01`
  (`oid` mediumint(9) NOT NULL DEFAULT '0',
                                       `pid` varchar(20) NOT NULL DEFAULT '',
                                                                          `quantita` smallint(6) DEFAULT NULL,
                                                                                                         `prezzo` float DEFAULT NULL,
                                                                                                                                `lsid` tinyint(4) NOT NULL,
                                                                                                                                                  PRIMARY KEY (`pid`,
                                                                                                                                                               `oid`,
                                                                                                                                                               `lsid`),
   FOREIGN KEY (`lsid`) REFERENCES `listino` (`lsid`) ON DELETE NO ACTION ON UPDATE NO ACTION,
   FOREIGN KEY (`pid`) REFERENCES `prodotto` (`pid`) ON DELETE CASCADE ON UPDATE CASCADE,
   FOREIGN KEY (`oid`) REFERENCES `ordine` (`oid`) ON DELETE CASCADE ON UPDATE CASCADE) ENGINE=InnoDB;


